// JavaScript mein kai tarah ke asynchronous functions hote hain, jo aapko non-blocking code execution aur concurrency provide karte hain. Yahan kuch pramukh asynchronous functions hain:

// Callbacks: Callbacks ek prachin tareeka hai asynchronous programming ka. Callback functions, ek function ke argument ke roop mein diye jaate hain, aur jab koi specific event occur hota hai (jaise ki file read ho jaana), toh callback function execute hota hai.

// Promises: Promises ek modern approach hai asynchronous programming ka. Ek promise, kisi future value ka representation hota hai, jaise ki kisi async operation ka completion ya failure. Promises ko .then() aur .catch() methods ke saath use kiya jaata hai.

// Async/Await: Async/await ek syntax-based approach hai asynchronous programming ka, jo ES6 mein introduce hua. async keyword se mark ki gayi functions mein aap await keyword ka istemal karke async operations ko synchronous-style mein handle kar sakte hain.

// Generators: Generators, special type ke functions hote hain jo execution ko suspend aur resume karne ki capability rakhte hain. Yeh asynchronous code ko likhne mein madad karte hain.

// Web APIs (like Fetch): Web APIs, jaise ki fetch, network requests ko asynchronous tareeke se handle karte hain. Yeh APIs aapko data fetching ke liye use karne mein madad karte hain.

// Timers (setTimeout, setInterval): Jaise ki aapne pehle pucha tha, timers bhi asynchronous functions hote hain jo specified time delay ya interval ke baad code ko execute karte hain.

// Event Listeners: Event listeners bhi asynchronous behavior provide karte hain. Jab koi event (jaise ki click, input change, etc.) occur hota hai, toh unka response execute hota hai.

// Yeh kuch pramukh asynchronous functions hain jo JavaScript mein available hote hain. Inka istemal aapko non-blocking aur responsive code likhne mein madad karta hai, jisse aap applications ko efficiently handle kar sakte hain.





